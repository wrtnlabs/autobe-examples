import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia, { tags } from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";
export * as revisions from "./revisions/index";

/**
 * Moderator: Soft-delete a post by ID (econ_political_forum_posts).
 *
 * Purpose and overview: Allows moderators to soft-delete posts by setting
 * econ_political_forum_posts.deleted_at. Moderator actions MUST be recorded in
 * econ_political_forum_moderation_logs with action_type, reason_code, and a
 * short rationale. The service MUST check econ_political_forum_legal_holds and
 * prohibit deletion when an active hold exists, returning 423 Locked and
 * recording the attempt in audit logs.
 *
 * Security and auditing:
 *
 * - Only active moderators (and administrators) should be permitted to call this
 *   endpoint. All moderator actions must be auditable via
 *   econ_political_forum_audit_logs and econ_political_forum_moderation_logs.
 *
 * Responses:
 *
 * - 204 No Content on success
 * - 403 Forbidden if caller lacks moderator privileges
 * - 423 Locked if legal hold prevents deletion
 * - 404 Not Found if post does not exist
 *
 * @param props.connection
 * @param props.postId Target post's ID (econ_political_forum_posts.id)
 * @path /econPoliticalForum/moderator/posts/:postId
 * @accessor api.functional.econPoliticalForum.moderator.posts.erase
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function erase(
  connection: IConnection,
  props: erase.Props,
): Promise<void> {
  return true === connection.simulate
    ? erase.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...erase.METADATA,
          path: erase.path(props),
          status: null,
        },
      );
}
export namespace erase {
  export type Props = {
    /** Target post's ID (econ_political_forum_posts.id) */
    postId: string & tags.Format<"uuid">;
  };

  export const METADATA = {
    method: "DELETE",
    path: "/econPoliticalForum/moderator/posts/:postId",
    request: null,
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = (props: Props) =>
    `/econPoliticalForum/moderator/posts/${encodeURIComponent(props.postId ?? "null")}`;
  export const random = (): void => typia.random<void>();
  export const simulate = (
    connection: IConnection,
    props: erase.Props,
  ): void => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: erase.path(props),
      contentType: "application/json",
    });
    try {
      assert.param("postId")(() => typia.assert(props.postId));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}
