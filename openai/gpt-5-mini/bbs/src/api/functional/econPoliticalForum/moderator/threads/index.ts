import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia, { tags } from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";
export * as tags from "./tags/index";

/**
 * Soft-delete (mark deleted_at) a thread by ID (econ_political_forum_threads) -
 * moderator.
 *
 * Marks the specified thread's deleted_at timestamp. Only active moderators
 * (authorizationRole=moderator) may delete content in moderation capacity per
 * policy. Server MUST record a moderation log entry with
 * action_type='soft_delete', reason_code and rationale. If a legal hold is
 * active, deletion is forbidden and returns 403. Successful response: 204 No
 * Content.
 *
 * Errors: 400 invalid UUID, 401 unauthenticated, 403 insufficient privileges or
 * legal hold, 404 not found, 429 rate limit, 500 server error.
 *
 * @param props.connection
 * @param props.threadId Unique identifier (UUID) of the target thread
 * @path /econPoliticalForum/moderator/threads/:threadId
 * @accessor api.functional.econPoliticalForum.moderator.threads.erase
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function erase(
  connection: IConnection,
  props: erase.Props,
): Promise<void> {
  return true === connection.simulate
    ? erase.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...erase.METADATA,
          path: erase.path(props),
          status: null,
        },
      );
}
export namespace erase {
  export type Props = {
    /** Unique identifier (UUID) of the target thread */
    threadId: string & tags.Format<"uuid">;
  };

  export const METADATA = {
    method: "DELETE",
    path: "/econPoliticalForum/moderator/threads/:threadId",
    request: null,
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = (props: Props) =>
    `/econPoliticalForum/moderator/threads/${encodeURIComponent(props.threadId ?? "null")}`;
  export const random = (): void => typia.random<void>();
  export const simulate = (
    connection: IConnection,
    props: erase.Props,
  ): void => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: erase.path(props),
      contentType: "application/json",
    });
    try {
      assert.param("threadId")(() => typia.assert(props.threadId));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}
