import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IShoppingMallCustomer } from "../../../../../structures/IShoppingMallCustomer";

/**
 * Complete customer email verification process
 * (shopping_mall_email_verifications, shopping_mall_customers).
 *
 * This endpoint verifies a customer's email address using a unique,
 * time-limited verification token issued by shopping_mall_email_verifications.
 * It checks the token's existence and expiry, updates the referenced customer's
 * email_verified field (shopping_mall_customers), and deletes the used
 * verification record.
 *
 * If the token is invalid or expired, a generic error is returned with no
 * information about account existence. This API does not require authentication
 * and only returns a status indicating the outcome, for security and privacy
 * compliance.
 *
 * Prisma schema references:
 * shopping_mall_email_verifications.token/user_id/expires_at,
 * shopping_mall_customers.email_verified/ id. Related endpoint:
 * requestEmailVerification to (re)start the workflow.
 *
 * @param props.connection
 * @param props.body Token for verifying customer email address.
 * @path /auth/customer/email/verify
 * @accessor api.functional.auth.customer.email.verify.verifyEmail
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function verifyEmail(
  connection: IConnection,
  props: verifyEmail.Props,
): Promise<verifyEmail.Response> {
  return true === connection.simulate
    ? verifyEmail.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...verifyEmail.METADATA,
          path: verifyEmail.path(),
          status: null,
        },
        props.body,
      );
}
export namespace verifyEmail {
  export type Props = {
    /** Token for verifying customer email address. */
    body: IShoppingMallCustomer.IVerifyEmail;
  };
  export type Body = IShoppingMallCustomer.IVerifyEmail;
  export type Response = IShoppingMallCustomer.IEmailVerificationResult;

  export const METADATA = {
    method: "POST",
    path: "/auth/customer/email/verify",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/customer/email/verify";
  export const random = (): IShoppingMallCustomer.IEmailVerificationResult =>
    typia.random<IShoppingMallCustomer.IEmailVerificationResult>();
  export const simulate = (
    connection: IConnection,
    props: verifyEmail.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: verifyEmail.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}
