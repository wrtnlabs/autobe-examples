import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IEconDiscussVerifiedExpertMfaVerify } from "../../../../../structures/IEconDiscussVerifiedExpertMfaVerify";
import { IEconDiscussVerifiedExpertMfa } from "../../../../../structures/IEconDiscussVerifiedExpertMfa";

/**
 * Verify MFA and set econ_discuss_users.mfa_enabled = true.
 *
 * After the user scans the provisioning URI, this endpoint verifies a submitted
 * one‑time code against econ_discuss_users.mfa_secret and on success sets
 * econ_discuss_users.mfa_enabled = true while persisting the secret and
 * recovery codes. It updates updated_at; other profile fields are untouched.
 *
 * If verification fails, do not enable mfa_enabled and return a generic error.
 * Successful verification completes MFA setup for the account, enabling MFA
 * checks during future /auth/verifiedExpert/login requests.
 *
 * @param props.connection
 * @param props.body One‑time code or recovery code to confirm setup.
 * @path /auth/verifiedExpert/mfa/verify
 * @accessor api.functional.auth.verifiedExpert.mfa.verify.verifyMfa
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function verifyMfa(
  connection: IConnection,
  props: verifyMfa.Props,
): Promise<verifyMfa.Response> {
  return true === connection.simulate
    ? verifyMfa.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...verifyMfa.METADATA,
          path: verifyMfa.path(),
          status: null,
        },
        props.body,
      );
}
export namespace verifyMfa {
  export type Props = {
    /** One‑time code or recovery code to confirm setup. */
    body: IEconDiscussVerifiedExpertMfaVerify.ICreate;
  };
  export type Body = IEconDiscussVerifiedExpertMfaVerify.ICreate;
  export type Response = IEconDiscussVerifiedExpertMfa.IStatus;

  export const METADATA = {
    method: "POST",
    path: "/auth/verifiedExpert/mfa/verify",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/verifiedExpert/mfa/verify";
  export const random = (): IEconDiscussVerifiedExpertMfa.IStatus =>
    typia.random<IEconDiscussVerifiedExpertMfa.IStatus>();
  export const simulate = (
    connection: IConnection,
    props: verifyMfa.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: verifyMfa.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}
