import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IEconDiscussMember } from "../../../../../../structures/IEconDiscussMember";

/**
 * Finalize password reset by updating econ_discuss_users.password_hash.
 *
 * This endpoint finalizes password recovery. After validating a reset token,
 * the server updates the userâ€™s credential by replacing password_hash in
 * econ_discuss_users and updating updated_at for auditing. No other fields
 * (such as email_verified, mfa_enabled) are changed here.
 *
 * Clients should require the user to re-authenticate after a reset. This
 * operation is complementary to /auth/member/password/reset and independent of
 * member role assignment in econ_discuss_members.
 *
 * @param props.connection
 * @param props.body Reset confirmation payload including token and new
 *   password.
 * @path /auth/member/password/reset/confirm
 * @accessor api.functional.auth.member.password.reset.confirm.confirmPasswordReset
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function confirmPasswordReset(
  connection: IConnection,
  props: confirmPasswordReset.Props,
): Promise<confirmPasswordReset.Response> {
  return true === connection.simulate
    ? confirmPasswordReset.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...confirmPasswordReset.METADATA,
          path: confirmPasswordReset.path(),
          status: null,
        },
        props.body,
      );
}
export namespace confirmPasswordReset {
  export type Props = {
    /** Reset confirmation payload including token and new password. */
    body: IEconDiscussMember.IPasswordResetConfirm;
  };
  export type Body = IEconDiscussMember.IPasswordResetConfirm;
  export type Response = IEconDiscussMember.ISecurityEvent;

  export const METADATA = {
    method: "POST",
    path: "/auth/member/password/reset/confirm",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/member/password/reset/confirm";
  export const random = (): IEconDiscussMember.ISecurityEvent =>
    typia.random<IEconDiscussMember.ISecurityEvent>();
  export const simulate = (
    connection: IConnection,
    props: confirmPasswordReset.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: confirmPasswordReset.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}
