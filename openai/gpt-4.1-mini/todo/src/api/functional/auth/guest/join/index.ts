import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { ITodoListGuest } from "../../../../structures/ITodoListGuest";

/**
 * Create a new guest user account with a unique nickname and issue tokens.
 *
 * Registers a new guest user by assigning a unique nickname, creating an entry
 * in the `todo_list_guests` table. This operation enables creation of temporary
 * guest accounts, which do not require password or email, aligning with the
 * guest role's non-authenticated access model. It issues JWT tokens for session
 * management.
 *
 * The `nickname` field is mandatory and must be unique as per schema
 * constraints. The operation uses the `id`, `nickname`, `created_at`, and
 * `updated_at` fields as part of the lifecycle.
 *
 * The guest account supports soft deletion via `deleted_at` field, which is
 * null upon creation.
 *
 * Upon success, it returns authorization tokens encapsulated in the
 * `ITodoListGuest.IAuthorized` DTO.
 *
 * This is the initial step for guests to gain temporary access, and works in
 * conjunction with the refresh token operation.
 *
 * @param props.connection
 * @param props.body Registration information for a new guest user including a
 *   unique nickname.
 * @setHeader token.access Authorization
 *
 * @path /auth/guest/join
 * @accessor api.functional.auth.guest.join.joinGuest
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function joinGuest(
  connection: IConnection,
  props: joinGuest.Props,
): Promise<joinGuest.Response> {
  const output: joinGuest.Response =
    true === connection.simulate
      ? joinGuest.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...joinGuest.METADATA,
            path: joinGuest.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace joinGuest {
  export type Props = {
    /**
     * Registration information for a new guest user including a unique
     * nickname.
     */
    body: ITodoListGuest.ICreate;
  };
  export type Body = ITodoListGuest.ICreate;
  export type Response = ITodoListGuest.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/guest/join",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/guest/join";
  export const random = (): ITodoListGuest.IAuthorized =>
    typia.random<ITodoListGuest.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: joinGuest.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: joinGuest.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}
