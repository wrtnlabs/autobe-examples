import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { ITodoListGuest } from "../../../../structures/ITodoListGuest";

/**
 * Refresh tokens for guest user to extend session validity.
 *
 * Refreshes authentication tokens for an existing guest user session. This
 * operation expects a valid refresh token to extend session validity without
 * requiring re-authentication.
 *
 * The operation relates to the `todo_list_guests` schema, reflecting guest
 * account lifecycle but no password verification.
 *
 * Ensures seamless continuation of guest sessions and token rotation.
 *
 * Success response is encapsulated in the `ITodoListGuest.IAuthorized` DTO.
 *
 * Works as a follow-up operation to the guest join endpoint.
 *
 * @param props.connection
 * @param props.body Refresh token request payload containing the refresh token.
 * @setHeader token.access Authorization
 *
 * @path /auth/guest/refresh
 * @accessor api.functional.auth.guest.refresh.refreshGuest
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function refreshGuest(
  connection: IConnection,
  props: refreshGuest.Props,
): Promise<refreshGuest.Response> {
  const output: refreshGuest.Response =
    true === connection.simulate
      ? refreshGuest.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...refreshGuest.METADATA,
            path: refreshGuest.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace refreshGuest {
  export type Props = {
    /** Refresh token request payload containing the refresh token. */
    body: ITodoListGuest.IRefresh;
  };
  export type Body = ITodoListGuest.IRefresh;
  export type Response = ITodoListGuest.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/guest/refresh",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/guest/refresh";
  export const random = (): ITodoListGuest.IAuthorized =>
    typia.random<ITodoListGuest.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: refreshGuest.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: refreshGuest.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}
