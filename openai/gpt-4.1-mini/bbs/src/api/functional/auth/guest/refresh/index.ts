import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IDiscussionBoardGuest } from "../../../../structures/IDiscussionBoardGuest";

/**
 * Refresh access tokens for guest sessions using refresh token, referencing
 * discussion_board_guests table.
 *
 * Refresh tokens for a guest session by validating the provided refresh token
 * and issuing new access tokens.
 *
 * Utilizes the discussion_board_guests table session_token and timestamps to
 * verify session validity.
 *
 * This operation supports seamless user experience for guest users with
 * temporary sessions.
 *
 * Related operation is join for initial guest session creation.
 *
 * Responses include new JWT tokens encapsulated in the authorized guest session
 * response body.
 *
 * @param props.connection
 * @param props.body Refresh token request information with refresh token
 *   string.
 * @setHeader token.access Authorization
 *
 * @path /auth/guest/refresh
 * @accessor api.functional.auth.guest.refresh.refreshGuest
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function refreshGuest(
  connection: IConnection,
  props: refreshGuest.Props,
): Promise<refreshGuest.Response> {
  const output: refreshGuest.Response =
    true === connection.simulate
      ? refreshGuest.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...refreshGuest.METADATA,
            path: refreshGuest.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace refreshGuest {
  export type Props = {
    /** Refresh token request information with refresh token string. */
    body: IDiscussionBoardGuest.IRefresh;
  };
  export type Body = IDiscussionBoardGuest.IRefresh;
  export type Response = IDiscussionBoardGuest.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/guest/refresh",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/guest/refresh";
  export const random = (): IDiscussionBoardGuest.IAuthorized =>
    typia.random<IDiscussionBoardGuest.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: refreshGuest.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: refreshGuest.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}
