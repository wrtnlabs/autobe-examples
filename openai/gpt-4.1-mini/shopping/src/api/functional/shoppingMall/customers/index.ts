import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IShoppingMallCustomer } from "../../../structures/IShoppingMallCustomer";

/**
 * Create a new customer account.
 *
 * Register a new customer account with the shopping mall platform.
 *
 * Client applications submit required email and password_hash, and optionally
 * include nickname and phone number. The status field typically starts as
 * 'active', but can vary.
 *
 * Email must be unique. Password hash must meet security requirements prior to
 * submission.
 *
 * Upon success, returns customer data excluding sensitive information.
 *
 * No authentication required for this public operation.
 *
 * Request body must comply with the defined DTO schema ensuring correct field
 * validation and formatting.
 *
 * @param props.connection
 * @param props.body Customer registration payload
 * @path /shoppingMall/customers
 * @accessor api.functional.shoppingMall.customers.create
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function create(
  connection: IConnection,
  props: create.Props,
): Promise<create.Response> {
  return true === connection.simulate
    ? create.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...create.METADATA,
          path: create.path(),
          status: null,
        },
        props.body,
      );
}
export namespace create {
  export type Props = {
    /** Customer registration payload */
    body: IShoppingMallCustomer.ICreate;
  };
  export type Body = IShoppingMallCustomer.ICreate;
  export type Response = IShoppingMallCustomer;

  export const METADATA = {
    method: "POST",
    path: "/shoppingMall/customers",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/shoppingMall/customers";
  export const random = (): IShoppingMallCustomer =>
    typia.random<IShoppingMallCustomer>();
  export const simulate = (
    connection: IConnection,
    props: create.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: create.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}
