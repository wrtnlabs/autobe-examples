/**
 * Represents a permanent account ban completely preventing platform access for
 * members who have committed severe or repeated violations of community
 * guidelines.
 *
 * Permanent bans are the most severe moderation action available on the
 * discussion board platform, issued exclusively by administrators for critical
 * violations including illegal content, explicit threats of violence, hate
 * speech, doxxing, or repeated violations after escalating warnings and
 * suspensions. Unlike temporary suspensions, bans completely block all platform
 * access and prevent the user from viewing content, logging in, or
 * participating in any capacity.
 *
 * Bans may be appealable or non-appealable based on violation severity.
 * Appealable bans allow users to submit formal appeals within a specified
 * appeal window (typically 30 days), while non-appealable bans are reserved for
 * the most egregious violations where no recourse is appropriate. When bans are
 * appealable and successfully appealed, the is_reversed flag is set and the
 * account access is restored.
 *
 * The ban record captures comprehensive documentation including the specific
 * violations, the administrator's detailed justification, the complete
 * violation history leading to the ban, and security measures like banned email
 * addresses and IP addresses to prevent re-registration. All ban decisions are
 * logged in moderation audit trails supporting transparency and administrative
 * oversight.
 *
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export type IDiscussionBoardBan = {
  /**
   * Primary key uniquely identifying this permanent ban record.
   *
   * This identifier is used to reference the ban in administrative
   * operations, appeal processes, and audit trail queries.
   */
  id: string & tags.Format<"uuid">;

  /**
   * Reference to the member account that has been permanently banned.
   *
   * Links to discussion_board_members.id representing the user who can no
   * longer access the platform due to severe or repeated violations of
   * community guidelines.
   */
  member_id: string & tags.Format<"uuid">;

  /**
   * Reference to the administrator who issued this permanent ban.
   *
   * Links to discussion_board_administrators.id representing the platform
   * administrator who made the decision to permanently ban this member after
   * thorough review of violations.
   */
  administrator_id: string & tags.Format<"uuid">;

  /**
   * Reference to the moderation action that initiated this permanent ban.
   *
   * Links to discussion_board_moderation_actions.id establishing the audit
   * trail from violation investigation through moderation decision to
   * permanent ban enforcement.
   */
  moderation_action_id: string & tags.Format<"uuid">;

  /**
   * Comprehensive justification explaining why this member was permanently
   * banned from the platform.
   *
   * Must be at least 100 characters to ensure thorough documentation of the
   * ban decision. This detailed explanation is provided to the banned user
   * and supports the appeal review process if the ban is marked as
   * appealable.
   */
  ban_reason: string;

  /**
   * Comprehensive summary documenting all violations and behavioral patterns
   * that led to the permanent ban decision.
   *
   * Provides historical context of the member's violation history including
   * prior warnings, suspensions, and the progression of policy violations
   * that culminated in permanent account termination.
   */
  violation_summary: string;

  /**
   * Indicates whether the banned user is permitted to submit a formal appeal
   * contesting this ban decision.
   *
   * Appeals are allowed for bans resulting from judgment calls or accumulated
   * violations, but prohibited for severe violations including illegal
   * content, explicit threats of violence, doxxing, or bans issued after
   * previous appeal denials.
   */
  is_appealable: boolean;

  /**
   * Number of days the banned user has to submit an appeal if the ban is
   * marked as appealable.
   *
   * Typically set to 30 days for appealable bans. Null for non-appealable
   * bans where the appeal process is not available. The appeal window begins
   * from the ban creation timestamp.
   */
  appeal_window_days?:
    | (number & tags.Type<"int32"> & tags.Minimum<1>)
    | null
    | undefined;

  /**
   * IP address captured at the time of ban to help prevent re-registration
   * attempts from the same network.
   *
   * Used for additional security measures to detect and prevent banned users
   * from creating new accounts. May be null if IP tracking was not available
   * or not deemed necessary.
   */
  ip_address_banned?: string | null | undefined;

  /**
   * Email address of the banned member that is prevented from future
   * registration.
   *
   * This email address is blocked from creating new accounts to prevent
   * banned users from immediately re-registering. The platform's registration
   * system checks against this banned email list.
   */
  email_banned: string & tags.Format<"email">;

  /**
   * Indicates whether this ban was reversed after successful appeal or
   * administrative review.
   *
   * When true, the ban decision was overturned and account access was
   * restored to the previously banned member. This flag supports ban history
   * tracking and moderation quality analysis.
   */
  is_reversed: boolean;

  /**
   * Timestamp when this ban was reversed and account access was restored.
   *
   * Null if the ban has not been reversed. Set when an appeal is approved or
   * an administrator overturns the ban decision upon review.
   */
  reversed_at?: (string & tags.Format<"date-time">) | null | undefined;

  /**
   * Comprehensive explanation of why this ban was reversed.
   *
   * Provides context for the reversal decision including what evidence or
   * arguments led to overturning the original ban. Required when is_reversed
   * is true, null otherwise.
   */
  reversal_reason?: string | null | undefined;

  /**
   * Timestamp when this permanent ban was issued and the member account was
   * blocked from platform access.
   *
   * Represents the exact moment the ban took effect and the member lost
   * access to all platform features except viewing their ban notification.
   */
  created_at: string & tags.Format<"date-time">;

  /**
   * Timestamp when this ban record was last modified.
   *
   * Updated when ban details are changed, when reversal information is added
   * after appeal approval, or when administrative corrections are made to ban
   * documentation.
   */
  updated_at: string & tags.Format<"date-time">;
};
